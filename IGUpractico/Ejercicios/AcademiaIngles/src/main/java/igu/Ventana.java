    private void botonAniadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAniadirActionPerformed
        String dni = txtDni.getText();
        String nom = txtNom.getText();
        String salario = txtSalario.getText();
        
        if(!validarDni(dni)){
            JOptionPane.showMessageDialog(this, "DNI invÃ¡lido. Debe tener 8 dÃ­gitos numÃ©ricos.");
        } else if (!validarNorepetirDni(dni)){
            JOptionPane.showMessageDialog(this, "Ese dni ya esta registrado");
        } else if (!validarNombre(nom)){
            JOptionPane.showMessageDialog(this, "Llene la casilla de nombre");
        } else if (!validarSalario(salario)){
            JOptionPane.showMessageDialog(this, "Salario invÃ¡lido. Ingrese un nÃºmero vÃ¡lido.");
        } else if(!optFijo.isSelected() && !optTemp.isSelected()){
            JOptionPane.showMessageDialog(this, "Selecciona un tipo de contrato");
        } else {
            
            boolean fijo = true;
            if (buttonGroup1.getSelection().equals(optTemp)){
                fijo = false;
            }
            
            academia.aniadirProfesor(new Profesor(Double.parseDouble(salario), fijo , dni, nom));
            JOptionPane.showMessageDialog(null, "Agregado exitosamente");
        }
        
        
    }//GEN-LAST:event_botonAniadirActionPerformed

    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
       String dni = txtDni.getText();
       
       if (dni.equals("")) {
           JOptionPane.showMessageDialog(null,"Ingrese el Dni");
       }else {
        boolean esta = false;
        Profesor p = null;
        for (Profesor c : academia.getProfesores()) {
            if (c.getDNI().equals(dni)) {
                esta = true;
                p=c;
            }
        }
           if (esta) {
               JOptionPane.showMessageDialog(null, "Profesor Encontrado: \n"+p.toString());
           } else {
               JOptionPane.showMessageDialog(null, "No esta registrado en la base de datos");
           }
        
        }
       
    }//GEN-LAST:event_botonBuscarActionPerformed

