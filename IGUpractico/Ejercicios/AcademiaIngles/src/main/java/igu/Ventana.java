    public boolean validarDni (String DNI){
        
        try {
            int numDni = Integer.parseInt(DNI);
            return DNI.length() == 8;
        } catch (Exception e) {
            return false;
        }
        
    }
    
    public boolean validarSalario (String salario) {
            try {
                double doublesalario = Double.parseDouble(salario);
                return doublesalario >= 0;
            } catch (Exception e) {
                return false;
            }
    }
    
    public boolean validarNombre (String nombre){
        return nombre != null;
    }
    public boolean validarNorepetirDni (String dni){
        return !academia.buscaContacto(dni);
    }


