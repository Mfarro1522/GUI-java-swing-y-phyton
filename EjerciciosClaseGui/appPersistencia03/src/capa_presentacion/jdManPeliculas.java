    private void listado() {
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Titulo");
        modelo.addColumn("Genero");
        modelo.addColumn("Director");
        modelo.addColumn("Actor");
        java.util.ArrayList<capa_logica.Pelicula> datos = capa_datos.PeliculaDAO.obtener();
        for (capa_logica.Pelicula p : datos) {
            modelo.addRow(new Object[] { p.getId(), p.getTitulo(), p.getGenero(), p.getDirector(), p.getActor() });
        }
        tablaPeliculas.setModel(modelo);
    }

    private void limpiar() {
        txtId.setText("");
        txtTitulo.setText("");
        comboTipo.setSelectedIndex(0);
        txtDirector.setText("");
        txtActor.setText("");
    }

    private void cargarDesdeTabla() {
        int fila = tablaPeliculas.getSelectedRow();
        if (fila >= 0) {
            int id = Integer.parseInt(tablaPeliculas.getValueAt(fila, 0).toString());
            java.util.ArrayList<capa_logica.Pelicula> datos = capa_datos.PeliculaDAO.obtener();
            for (capa_logica.Pelicula p : datos) {
                if (p.getId() == id) {
                    txtId.setText(String.valueOf(p.getId()));
                    txtTitulo.setText(p.getTitulo());
                    comboTipo.setSelectedItem(p.getGenero());
                    txtDirector.setText(p.getDirector());
                    txtActor.setText(p.getActor());
                    break;
                }
            }
        }
    }

    private void agregarPelicula() {
        if (txtTitulo.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Complete el titulo");
            return;
        }
        int id = capa_datos.PeliculaDAO.obtener().size() + 1;
        capa_logica.Pelicula p = new capa_logica.Pelicula(
                id,
                txtTitulo.getText(),
                comboTipo.getSelectedItem().toString(),
                txtDirector.getText(),
                txtActor.getText(),
                new java.util.Date(),
                0);
        capa_datos.PeliculaDAO.agrega(p);
        capa_datos.PeliculaDAO.grabar();
        javax.swing.JOptionPane.showMessageDialog(this, "Pelicula agregada");
        listado();
        limpiar();
    }

    private void modificarPelicula() {
        if (txtId.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una pelicula");
            return;
        }
        int id = Integer.parseInt(txtId.getText());
        java.util.ArrayList<capa_logica.Pelicula> datos = capa_datos.PeliculaDAO.obtener();
        for (int i = 0; i < datos.size(); i++) {
            if (datos.get(i).getId() == id) {
                capa_logica.Pelicula p = new capa_logica.Pelicula(
                        id,
                        txtTitulo.getText(),
                        comboTipo.getSelectedItem().toString(),
                        txtDirector.getText(),
                        txtActor.getText(),
                        datos.get(i).getFechaEstreno(),
                        datos.get(i).getPresupuesto());
                datos.set(i, p);
                capa_datos.PeliculaDAO.grabar();
                javax.swing.JOptionPane.showMessageDialog(this, "Pelicula modificada");
                listado();
                limpiar();
                break;
            }
        }
    }

    private void eliminarPelicula() {
        if (txtId.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una pelicula");
            return;
        }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Â¿Eliminar pelicula?");
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(txtId.getText());
            java.util.ArrayList<capa_logica.Pelicula> datos = capa_datos.PeliculaDAO.obtener();
            for (int i = 0; i < datos.size(); i++) {
                if (datos.get(i).getId() == id) {
                    datos.remove(i);
                    capa_datos.PeliculaDAO.grabar();
                    javax.swing.JOptionPane.showMessageDialog(this, "Pelicula eliminada");
                    listado();
                    limpiar();
                    break;
                }
            }
        }
    }

