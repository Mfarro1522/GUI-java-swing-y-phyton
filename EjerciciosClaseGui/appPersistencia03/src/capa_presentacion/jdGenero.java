    private void listado() {
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Descripcion");
        java.util.ArrayList<capa_logica.Genero> datos = capa_datos.GeneroDAO.obtener();
        for (capa_logica.Genero g : datos) {
            modelo.addRow(new Object[] { g.getId(), g.getDescripcion() });
        }
        tablaGenero.setModel(modelo);
    }

    private void limpiar() {
        txtId.setText("");
        txtDescripcion.setText("");
    }

    private void cargarDesdeTabla() {
        int fila = tablaGenero.getSelectedRow();
        if (fila >= 0) {
            int id = Integer.parseInt(tablaGenero.getValueAt(fila, 0).toString());
            java.util.ArrayList<capa_logica.Genero> datos = capa_datos.GeneroDAO.obtener();
            for (capa_logica.Genero g : datos) {
                if (g.getId() == id) {
                    txtId.setText(String.valueOf(g.getId()));
                    txtDescripcion.setText(g.getDescripcion());
                    break;
                }
            }
        }
    }

    private void agregarGenero() {
        if (txtDescripcion.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Complete la descripcion");
            return;
        }
        int id = capa_datos.GeneroDAO.obtener().size() + 1;
        capa_logica.Genero g = new capa_logica.Genero(id, txtDescripcion.getText());
        capa_datos.GeneroDAO.agrega(g);
        capa_datos.GeneroDAO.grabar();
        javax.swing.JOptionPane.showMessageDialog(this, "Genero agregado");
        listado();
        limpiar();
    }

    private void modificarGenero() {
        if (txtId.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un genero");
            return;
        }
        int id = Integer.parseInt(txtId.getText());
        java.util.ArrayList<capa_logica.Genero> datos = capa_datos.GeneroDAO.obtener();
        for (int i = 0; i < datos.size(); i++) {
            if (datos.get(i).getId() == id) {
                capa_logica.Genero g = new capa_logica.Genero(id, txtDescripcion.getText());
                datos.set(i, g);
                capa_datos.GeneroDAO.grabar();
                javax.swing.JOptionPane.showMessageDialog(this, "Genero modificado");
                listado();
                limpiar();
                break;
            }
        }
    }

    private void eliminarGenero() {
        if (txtId.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un genero");
            return;
        }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Â¿Eliminar genero?");
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(txtId.getText());
            java.util.ArrayList<capa_logica.Genero> datos = capa_datos.GeneroDAO.obtener();
            for (int i = 0; i < datos.size(); i++) {
                if (datos.get(i).getId() == id) {
                    datos.remove(i);
                    capa_datos.GeneroDAO.grabar();
                    javax.swing.JOptionPane.showMessageDialog(this, "Genero eliminado");
                    listado();
                    limpiar();
                    break;
                }
            }
        }
    }

