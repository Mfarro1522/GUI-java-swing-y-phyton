    private void listado() {
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Nacionalidad");
        java.util.ArrayList<capa_logica.Actor> datos = capa_datos.ActorDAO.obtener();
        for (capa_logica.Actor a : datos) {
            modelo.addRow(new Object[] { a.getId(), a.getNombre(), a.getPais() });
        }
        tablaActor.setModel(modelo);
    }

    private void limpiar() {
        txtId.setText("");
        txtNombre.setText("");
        txtNacionalidad.setText("");
    }

    private void cargarDesdeTabla() {
        int fila = tablaActor.getSelectedRow();
        if (fila >= 0) {
            int id = Integer.parseInt(tablaActor.getValueAt(fila, 0).toString());
            java.util.ArrayList<capa_logica.Actor> datos = capa_datos.ActorDAO.obtener();
            for (capa_logica.Actor a : datos) {
                if (a.getId() == id) {
                    txtId.setText(String.valueOf(a.getId()));
                    txtNombre.setText(a.getNombre());
                    txtNacionalidad.setText(a.getPais());
                    break;
                }
            }
        }
    }

    private void agregarActor() {
        if (txtNombre.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Complete el nombre");
            return;
        }
        int id = capa_datos.ActorDAO.obtener().size() + 1;
        capa_logica.Actor a = new capa_logica.Actor(id, txtNombre.getText(), "", txtNacionalidad.getText());
        capa_datos.ActorDAO.agrega(a);
        capa_datos.ActorDAO.grabar();
        javax.swing.JOptionPane.showMessageDialog(this, "Actor agregado");
        listado();
        limpiar();
    }

    private void modificarActor() {
        if (txtId.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un actor");
            return;
        }
        int id = Integer.parseInt(txtId.getText());
        java.util.ArrayList<capa_logica.Actor> datos = capa_datos.ActorDAO.obtener();
        for (int i = 0; i < datos.size(); i++) {
            if (datos.get(i).getId() == id) {
                capa_logica.Actor a = new capa_logica.Actor(id, txtNombre.getText(), "", txtNacionalidad.getText());
                datos.set(i, a);
                capa_datos.ActorDAO.grabar();
                javax.swing.JOptionPane.showMessageDialog(this, "Actor modificado");
                listado();
                limpiar();
                break;
            }
        }
    }

    private void eliminarActor() {
        if (txtId.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un actor");
            return;
        }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Â¿Eliminar actor?");
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(txtId.getText());
            java.util.ArrayList<capa_logica.Actor> datos = capa_datos.ActorDAO.obtener();
            for (int i = 0; i < datos.size(); i++) {
                if (datos.get(i).getId() == id) {
                    datos.remove(i);
                    capa_datos.ActorDAO.grabar();
                    javax.swing.JOptionPane.showMessageDialog(this, "Actor eliminado");
                    listado();
                    limpiar();
                    break;
                }
            }
        }
    }

