    public void generarCaptcha (){
        captcha = new Captcha.Builder(210, 50)
                .addText()
                .addBackground()
                .addNoise()
                .gimp(new FishEyeGimpyRenderer()) //-> genera error .gimp() solo ; pero solo en consola usalo si quieres 
                //segun su documentacion agrega distorcion esta de ojo de pez no tiene errores
                .addBorder()
                .build();
    }
    
    public boolean confirmarCaptcha(){
        if(captcha.getAnswer().equals(confirmcaptcha.getText())){
            return true;
        }else {
            return false;
        }
    }
